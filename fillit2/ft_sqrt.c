int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb <= 0)
		return (0);
	else if (nb > 2147395600)
		return (0);
	while (i * i < nb)
	{
		i++;
	}
	if (nb == 0)
		return (0);
	else
		return (i);
}
